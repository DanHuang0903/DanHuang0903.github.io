function doChange(x){
			if(x.id == "addimg"){
				document.getElementById("add").style.color = "#804050";
			}
			else if(x.id == "qqimg"){
				document.getElementById("qq").style.color = "#804050";
			}
			else if(x.id == "emailimg"){
				document.getElementById("email").style.color = "#804050";
			}
		}
		
		function doBack(x){
			if(x.id == "addimg"){
				document.getElementById("add").style.color = "#00477D";
			}
			else if(x.id == "qqimg"){
				document.getElementById("qq").style.color = "#00477D";
			}
			else if(x.id == "emailimg"){
				document.getElementById("email").style.color = "#00477D";
			}
		}
		
		